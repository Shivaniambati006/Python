#text type :- string
x = "shivani"
print(type(x))

#Numeric Type :- Integer,float,complex
x1 = 20
print(type(x1))

x2 = 20.34
print(type(x2))

x3 = 4j
print(type(x3))

#Sequence Types :- list, tuple, range

x4 = ["apple", "banana", "cherry"]
print(type(x4))

x5 = ("apple", "banana", "cherry")
print(type(x5))

x6 = range(6)
print(type(x6))
